using System;
using System.Collections.Generic;
using System.Text;

namespace HelloWorldWithClass
{
    class Calculator
    {
        public int plus(int n1, int n2)
        {
            return n1 + n2;
        }

        public int minus(int n1, int n2)
        {
            return n1 - n2;
        }

        public int intu(int n1, int n2)
        {
            return n1 * n2;
        }

        public int division(int n1, int n2)
        {
            return n1 / n2;


        }
    }
}